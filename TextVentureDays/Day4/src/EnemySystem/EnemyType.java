package EnemySystem;

/**
 * EnemyType defines the different types of enemies that can be created.
 * This is used by the EnemyFactory to determine which enemy instance to instantiate.
 */
public enum EnemyType {
    /** A standard goblin enemy with balanced stats. */
    GOBLIN,

    /** A stronger variant of the goblin with higher attack and defense. */
    HOB_GOBLIN,

}

